var n1 = parseInt(prompt("Digite o primeiro número: "));
var n2 = parseInt(prompt("Digite o segundo número : "));

if (n1 > n2) {
    alert("O primeiro número: " + n1 + " é maior")
}
if (n2 > n1) {
    alert("O segundo número: " + n2 + " é maior")
}
if (n1 == n2) {
    alert("Os dois números são iguais: ")
}
